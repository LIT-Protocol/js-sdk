import path from 'path';
import { success, fail, testThis } from '../../tools/scripts/utils.mjs';
import LITCONFIG from '../../lit.config.json' assert { type: 'json' };
import { client } from '../00-setup.mjs';
import { LitAbility, LitActionResource } from '@lit-protocol/auth-helpers';
import { LitAuthClient } from '@lit-protocol/lit-auth-client';
import { AuthMethodType, ProviderType } from '@lit-protocol/constants';
import { ethers } from 'ethers';
import { PKPEthersWallet } from '@lit-protocol/pkp-ethers';

// NOTE: you need to hash data before you send it in.
// If you send something that isn't 32 bytes, the nodes will return an error.
const TO_SIGN = ethers.utils.arrayify(ethers.utils.keccak256([1, 2, 3, 4, 5]));

export async function main() {
  // ==================== Setup ====================

  const litAuthClient = new LitAuthClient({
    litRelayConfig: {
      relayApiKey: '67e55044-10b1-426f-9247-bb680e5fe0c8_relayer',
    },
    version: 'V3',
    litNodeClient: client,
  });

  // -- eth wallet
  const authProvider = litAuthClient.initProvider(ProviderType.EthWallet);
  const authMethod = {
    authMethodType: AuthMethodType.EthWallet,
    accessToken: JSON.stringify(LITCONFIG.CONTROLLER_AUTHSIG_2),
  };

  // -- setting scope for the auth method
  // https://developer.litprotocol.com/v3/sdk/wallets/auth-methods/#auth-method-scopes
  const options = {
    permittedAuthMethodScopes: [[1]],
  };

  let pkps = await authProvider.fetchPKPsThroughRelayer(authMethod);

  if (pkps.length <= 0) {
    try {
      const auth = await authProvider.mintPKPThroughRelayer(
        authMethod,
        options
      );
      console.log('auth', auth);
    } catch (e) {
      return fail('Failed to mint PKP');
    }
    pkps = await authProvider.fetchPKPsThroughRelayer(authMethod);
  }

  const pkp = pkps[pkps.length - 1];

  // convert BigNumber to string
  pkp.tokenId = ethers.BigNumber.from(pkp.tokenId).toString();

  console.log('pkp', pkp);

  const pkpPubKey = pkp.publicKey;

  const sessionKeyPair = client.getSessionKey();

  const authNeededCallback = async (params) => {
    const response = await client.signSessionKey({
      sessionKey: sessionKeyPair,
      statement: params.statement,
      authSig: JSON.parse(authMethod.accessToken), // When this is empty or undefined, it will fail
      authMethods: [authMethod],
      pkpPublicKey: pkpPubKey,
      expiration: params.expiration,
      resources: params.resources,
      chainId: 1,
    });
    return response.authSig;
  };

  const resourceAbilities = [
    {
      resource: new LitActionResource('*'),
      ability: LitAbility.PKPSigning,
    },
  ];

  // ==================== Test Logic ====================

  const sessionSigs = await client.getSessionSigs({
    chain: 'ethereum',
    expiration: new Date(Date.now() + 1000 * 60 * 60 * 24 * 7).toISOString(),
    resourceAbilityRequests: resourceAbilities,
    sessionKey: sessionKeyPair,
    authNeededCallback,
  });

  const pkpSignRes = await client?.pkpSign({
    toSign: TO_SIGN,
    pubKey: pkpPubKey,
    sessionSigs: sessionSigs,
    // authMethods: [authMethod], // This is not working!
  });

  const pkpWallet = new PKPEthersWallet({
    pkpPubKey: pkpPubKey,
    controllerSessionSigs: sessionSigs,
    controllerAuthMethods: [],
  });

  await pkpWallet.init();

  const signature = await pkpWallet.signMessage(TO_SIGN);

  // ==================== Post-Validation ====================

  if (!pkpSignRes) {
    return fail(
      'Failed to sign data with sessionSigs generated by eth wallet auth method'
    );
  }

  let missingKeys = [];

  if (pkpSignRes) {
    ['r', 's', 'recid', 'signature', 'publicKey', 'dataSigned'].forEach(
      (key) => {
        if (pkpSignRes[key] === undefined) {
          missingKeys.push(key);
        }
      }
    );
  }

  if (missingKeys.length > 0) {
    return fail(`Missing keys: ${missingKeys.join(', ')}`);
  }

  if (!signature) {
    return fail(
      'Failed to sign data with sessionSigs generated by eth wallet auth method'
    );
  }

  // ==================== Success ====================
  return success(
    `it should use sessionSigs generated by eth wallet auth method to sign data. Signature is ${signature} and pkpSignRes is ${JSON.stringify(
      pkpSignRes
    )}`
  );
}

await testThis({ name: path.basename(import.meta.url), fn: main });
